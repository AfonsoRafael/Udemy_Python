# sep define os separadores entre os valores
# end define o que será colocado ao final da impressão
print(12, 34, 1011, sep="", end='#')# sem espaço e com # no final
print(56, 78, sep="", end='#')# sem espaço e com # no final
print(56, 78, sep='-', end='\n')# padrao é espaço e quebra de linha
print(9, 10, sep='-', end='\n')